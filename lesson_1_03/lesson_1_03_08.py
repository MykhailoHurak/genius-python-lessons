# for
# if, elif, else

list_1 = ["python", "JS", "js" "TS", "Python", "C++", "C#"]

for element in list_1:
    if element == "Python":
        print(f"element = {element}")
    elif element == "JS":
        print(f"element = {element}")
    elif element == "C#":
        print(f"element is {element}")
    else:
        print(f"Element '{element}' we did not look for")