# Створити змінні таких типів: string, integer, float, bool, list, dict, tuple, None

my_string = "My name is Michael. I'm from Ukraine. I'm a Frontend Developer"
print("my_string:", my_string)
print("Type of my_string:", type(my_string))

my_age = 33
print("my_age:", my_age)
print("Type of my_age:", type(my_age))

my_weight = 85.5
print("my_weight:", my_weight)
print("Type of my_weight:", type(my_weight))

have_animal = False
print("have_animal:", have_animal)
print("Type of have_animal:", type(have_animal))

my_hard_skills = ["HTML+CSS", "JavaScript", "Python"]
print("my_hard_skills:", my_hard_skills)
print("Type of my_hard_skills:", type(my_hard_skills))

my_favourite_numbers = (1, 5, 10)
print("my_favourite_numbers:", my_favourite_numbers)
print("Type of my_favourite_numbers:", type(my_favourite_numbers))

my_variable_None = None
print("my_variable_None:", my_variable_None)
print("Type of my_variable_None:", type(my_variable_None))