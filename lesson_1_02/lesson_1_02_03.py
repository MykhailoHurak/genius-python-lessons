# Типи данних

num_1 = 5
print(type(num_1))

num_2 = 3.14
print(type(num_2))

string = "Real"
print(type(string))

example_check = True
print(type(example_check))

example_list = ["My", "name", "is", "Michael"]
print(type(example_list))

example_tuple = (1, 2, 3)
print(type(example_tuple))

example_dictionary = {"name": "Mykhailo", "age": 33}
print(type(example_dictionary))

example_set = {1, 2, 3, 4}
print(type(example_set))

print(None)

class Person:
    pass

a = Person()
print(type(a))